DEST = r"C:\My Project\kosmocapital\public\photos\migrated"
os.makedirs(DEST, exist_ok=True)

def download(url):
    fname = url.split("/")[-1].lower()
    path = os.path.join(DEST, fname)
    if os.path.exists(path):
        return f"SKIP {fname}"
    try:
        req = urllib.request.Request(url, headers={"User-Agent": "Mozilla/5.0"})
        with urllib.request.urlopen(req, timeout=15) as r:
            data = r.read()
        with open(path, "wb") as f:
            f.write(data)
        return f"OK {fname} ({len(data)//1024}KB)"
    except Exception as e:
        return f"ERR {fname}: {e}"

with concurrent.futures.ThreadPoolExecutor(max_workers=20) as ex:
    results = list(ex.map(download, URLS))

ok = [r for r in results if r.startswith("OK")]
err = [r for r in results if r.startswith("ERR")]
skip = [r for r in results if r.startswith("SKIP")]
print(f"Downloaded: {len(ok)}, Skipped: {len(skip)}, Errors: {len(err)}")
for e in err:
    print(e)
